
public class PojoVehicles {
	private float id_autonumeric;
	private float id_vehicle;
	private float id_fabricant;
	private float id_fichatecnica;
	private float id_concesionari;
	private String model;
	private String versio;
	
	public PojoVehicles(){}
	
	public float getId_autonumeric() {
		return id_autonumeric;
	}
	public void setId_autonumeric(float id_autonumeric) {
		this.id_autonumeric = id_autonumeric;
	}
	public float getId_vehicle() {
		return id_vehicle;
	}
	public void setId_vehicle(float id_vehicle) {
		this.id_vehicle = id_vehicle;
	}
	public float getId_fabricant() {
		return id_fabricant;
	}
	public void setId_fabricant(float id_fabricant) {
		this.id_fabricant = id_fabricant;
	}
	public float getId_fichatecnica() {
		return id_fichatecnica;
	}
	public void setId_fichatecnica(float id_fichatecnica) {
		this.id_fichatecnica = id_fichatecnica;
	}
	public float getId_concesionari() {
		return id_concesionari;
	}
	public void setId_concesionari(float id_concesionari) {
		this.id_concesionari = id_concesionari;
	}
	public String getModel() {
		return model;
	}
	public void setModel(String model) {
		this.model = model;
	}
	public String getVersio() {
		return versio;
	}
	public void setVersio(String versio) {
		this.versio = versio;
	}
	
	
}
