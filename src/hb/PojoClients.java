
public class PojoClients {
	private float id_autonumeric;
	private float id_concesionari;
	private float id_adreça;
	private String id_client;
	private String nom;
	private int telefon;
	
	public PojoClients(){}

	public float getId_autonumeric() {
		return id_autonumeric;
	}

	public void setId_autonumeric(float id_autonumeric) {
		this.id_autonumeric = id_autonumeric;
	}

	public float getId_concesionari() {
		return id_concesionari;
	}

	public void setId_concesionari(float id_concesionari) {
		this.id_concesionari = id_concesionari;
	}

	public float getId_adreça() {
		return id_adreça;
	}

	public void setId_adreça(float id_adreça) {
		this.id_adreça = id_adreça;
	}

	public String getId_client() {
		return id_client;
	}

	public void setId_client(String id_client) {
		this.id_client = id_client;
	}

	public String getNom() {
		return nom;
	}

	public void setNom(String nom) {
		this.nom = nom;
	}

	public int getTelefon() {
		return telefon;
	}

	public void setTelefon(int telefon) {
		this.telefon = telefon;
	}
	
	
	
}
