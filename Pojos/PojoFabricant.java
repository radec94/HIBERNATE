
public class PojoFabricant {
	private float id_autonumeric;
	private float id_fabricant;
	private String nomFabricant;
	private String pais;
	
	public PojoFabricant(){}

	public float getId_autonumeric() {
		return id_autonumeric;
	}

	public void setId_autonumeric(float id_autonumeric) {
		this.id_autonumeric = id_autonumeric;
	}

	public float getId_fabricant() {
		return id_fabricant;
	}

	public void setId_fabricant(float id_fabricant) {
		this.id_fabricant = id_fabricant;
	}

	public String getNomFabricant() {
		return nomFabricant;
	}

	public void setNomFabricant(String nomFabricant) {
		this.nomFabricant = nomFabricant;
	}

	public String getPais() {
		return pais;
	}

	public void setPais(String pais) {
		this.pais = pais;
	}
	
	
}
