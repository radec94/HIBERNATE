
public class PojoAdreca {
	private float id_autonumeric;
	private float id_adreca;
	private String nom;
	private int cp;
	private String poblacio;
	
	public PojoAdreca(){}

	public float getId_autonumeric() {
		return id_autonumeric;
	}

	public void setId_autonumeric(float id_autonumeric) {
		this.id_autonumeric = id_autonumeric;
	}

	public float getId_adreca() {
		return id_adreca;
	}

	public void setId_adreca(float id_adreca) {
		this.id_adreca = id_adreca;
	}

	public String getNom() {
		return nom;
	}

	public void setNom(String nom) {
		this.nom = nom;
	}

	public int getCp() {
		return cp;
	}

	public void setCp(int cp) {
		this.cp = cp;
	}

	public String getPoblacio() {
		return poblacio;
	}

	public void setPoblacio(String poblacio) {
		this.poblacio = poblacio;
	}
	
	
}
