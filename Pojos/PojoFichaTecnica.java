
public class PojoFichaTecnica {
	private float id_autonumeric;
	private float id_vehicle;
	private int altura;
	private int amplada;
	private String descripcio;
	
	public PojoFichaTecnica(){}

	public float getId_autonumeric() {
		return id_autonumeric;
	}

	public void setId_autonumeric(float id_autonumeric) {
		this.id_autonumeric = id_autonumeric;
	}

	public float getId_vehicle() {
		return id_vehicle;
	}

	public void setId_vehicle(float id_vehicle) {
		this.id_vehicle = id_vehicle;
	}

	public int getAltura() {
		return altura;
	}

	public void setAltura(int altura) {
		this.altura = altura;
	}

	public int getAmplada() {
		return amplada;
	}

	public void setAmplada(int amplada) {
		this.amplada = amplada;
	}

	public String getDescripcio() {
		return descripcio;
	}

	public void setDescripcio(String descripcio) {
		this.descripcio = descripcio;
	}
	
	
	
}
